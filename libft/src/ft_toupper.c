int	ft_toupper(int c)
{
	if (c < 0)
	{
		return (c);
	}
	if (c >= 0 && c <= 255)
	{
		if (c >= 'a' && c <= 'z')
		{
			return (c - 32);
		}
	}
	return (c);
}
